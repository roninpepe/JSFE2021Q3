import { EventCallback, EventHandler } from '../types/utils';

function $(selector: string, scope: HTMLElement | Document = document): HTMLElement {
  return <HTMLElement>scope.querySelector(selector);
}

export function EventCallbackBuilder(callback: EventCallback, className?: string): EventHandler {
  const handler: EventHandler = {
    handleEvent(event: Event) {
      if (className) {
        if (event.target && (<Element>event.target).classList.contains(className)) {
          callback(event);
        }
      } else {
        callback(event);
      }
    },
  };
  return handler;
}

export function toggleArrayItem<T>(array: Array<T>, value: T): void {
  const index: number = array.indexOf(value);
  if (index === -1) {
    array.push(value);
  } else {
    array.splice(index, 1);
  }
}

